            cart[key] = true
            elem.classList.remove("bi-heart")
            elem.classList.add("bi-heart-fill")
        }else {
            cart[key] = false
            elem.classList.add("bi-heart")
            elem.classList.remove("bi-heart-fill")
        }
        localStorage.setItem('data', JSON.stringify(cart))
    }
</script>
